import { createRequire } from "node:module";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { build as esbuild } from "esbuild";

globalThis.require = createRequire(import.meta.url);

// No esbuild-plugin-pino here: logger.ts only enables the pino-pretty transport
// when NODE_ENV !== "production", and Vercel always runs functions with
// NODE_ENV=production, so the transport (and its worker-thread bundling
// requirement) is never reached at runtime.

